      <li><b style="color:#ff9900">推理设备</b>: 至少要求浏览器支持WebGPU, WebASM，WebGL的其中一个。确保浏览器满足下列版本要求。
        <ul>
          <li>Edge: >=15</li>
          <li>Chrome: >=58</li>
          <li>Safari: >=10.1</li>
          <li>FireFox: >=53</li>
        </ul>
      </li>
      <li><b style="color:#ff9900">WebCAM</b>: 至少要求浏览器支持navigator.mediaDevices。确保浏览器满足下列版本要求。
        <ul>
          <li>Edge: >=12 (若要确保隐私安全，则>=79)</li>
